if(x > 1) {
    // si le test est vrai
    alert('IF  x contient ' + x + ' et cette valeur est supérieure à 1');
} else {
    // sinon j'exécute celui-ci
    alert('ELSE  x contient ' + x + ' et cette valeur est inférieure à 1');
}
